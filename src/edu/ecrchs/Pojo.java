package edu.ecrchs;

public class Pojo {
    private int one;
    private int two;

    public Pojo(int one, int two) {
        this.one = one;
        this.two = two;
    }

    public int getTwo() {
        return two;
    }

    public void setTwo(int two) {
        this.two = two;
    }

    public int getOne() {
        return one;
    }

    public void setOne(int one) {
        this.one = one;
    }
}
